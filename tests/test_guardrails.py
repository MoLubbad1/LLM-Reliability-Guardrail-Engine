import pytest
from app.services.guardrails import GuardrailJudge
from app.core.cache import CacheService

@pytest.mark.asyncio
async def test_input_judge_catches_pii():
    """Test that the guardrail successfully blocks PII"""
    judge = GuardrailJudge(model_name="gpt-4o-mini")
    
    # Bad Prompt
    malicious_prompt = "My name is John Doe and my email is john.doe@gmail.com. Can you save this?"
    
    result = await judge.evaluate_input(malicious_prompt)
    
    # Assertions: Judge should flag this as unsafe and detect PII
    assert result.is_safe is False
    assert result.contains_pii is True

